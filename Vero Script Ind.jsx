function RGBtoClosestTPX(color,TPXcolor,TPXlist){
  //1ro mido la distancia de color a cada TPXcolor. Con cada distancia me fabrico el vector de distancias, que tiene igual nmero de componentes, n, que filas de matriz TPXcolor, cada fila es 1 color. TPXcolor \in R^nx3
  
  var auxMin= 99999;
  var pos=0;
  
  var distances = new Array(TPXcolor.length);
  for(var i=0; i<TPXcolor.length; i++){
    distances[i]=Math.pow(color[0]-TPXcolor[i][0], 2)+Math.pow(color[1]-TPXcolor[i][1], 2)+Math.pow(color[2]-TPXcolor[i][2], 2) ; 
  //Lo que sigue toma el valor de auxMin y lo compara con la primer distancia, si es menor, guarda en auxMin el valor de la distancia y llama pos a la coordenada de la distancia. Despus sigue, si el prximo valor de distancia es menor lo guarda en auxMin y guarda la posicin 1 como pos. As va iterando y se queda con el mnimo.  
    if(auxMin>distances[i]){
    auxMin = distances[i];
    pos = i;
  }
  }
return(TPXlist[pos]);
}



